public class sala {

    private int id;
    private String nome;
    private String lotacao;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getlotacao() {
        return lotacao;
    }

    public void setlotacao(String lotacao) {
        this.lotacao = lotacao;
    }
    
     
}